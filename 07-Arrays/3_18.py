import MyArrays

arr = [7,3,8,5,2]

print(f"Second largest number: {MyArrays.second_largest(arr)}")
print(f"Difference between smallest and largest numbers: {MyArrays.difference(arr)}")
print(f"Median: {MyArrays.median(arr)}")
print(f"Smallest and largest number: {MyArrays.smallest_and_largest(arr)[0]}, {MyArrays.smallest_and_largest(arr)[1]}")
print(f"Numbers as a string: {MyArrays.separated(arr)}")