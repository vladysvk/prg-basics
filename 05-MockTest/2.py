temperature = 35  # In Celsius

if temperature > 30:
    print("It's hot outside. Stay hydrated!")
elif temperature > 20:
    print("The weather is nice. How about a walk?")
elif temperature > 10:
    print("It's a bit chilly. You might need a jacket.")
else:
    print("It's cold! Stay indoors or bundle up!")

